int main(void)
{ 
  HAL_Init();             					//初始化HAL库  
	UART1_Init(115200);								//初始化串口1
	UART2_Init(115200);								//初始化串口2
	Rs485_Init();   									//初始化485
	TIM3_Init(1000-1,64-1);						//初始化定时器3
	ESP8266_Init();										//初始化ESP8266
	
	while(1)
	{
		USART1_IRQHand();								//接收串口二的数据发送到485中
		USART2_IRQHand();								//接收485数据发送到串口二中
	}
}

